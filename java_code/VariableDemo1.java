package com.it.code_for_day01;
public class VariableDemo1 {
    //主入口
    public static void main(String[] args) {
        //定義變量
        //數據類型 變量名 = 數據值
        //數據類型: 限定了變量能存储數據的類型
        //int(整數)、double(小數)
        //變量名: 就是存储空間名稱
        //作用: 方便以後使用
        //數據值: 真正存在變量中的數據

        //等號: 賦值。把右邊的數據值賦值給左邊的變量

        int a = 10;
        System.out.println(a); //10
    }
}
