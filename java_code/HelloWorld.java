/*
    java程序中最基本的組成單位是 類。

    類 的定義格式:
        public class 類名 {

        }

    這是我定義一個HelloWorld類
 */
package com.it.code_for_day01;
public class HelloWorld {
    /*
        這是 main方法
        main方法 是程序的入口方法，代碼執行是從 main方法 開始的l
    */
    public static void main(String[] args) {
        // 這是輸出語句，""裡面的內容是可以改變的，將()中的內容輸出打印。
        System.out.println("HelloWorld");
    }
}
